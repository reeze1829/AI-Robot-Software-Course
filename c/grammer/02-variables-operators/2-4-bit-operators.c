/*
 * 주제: 2-4. 비트 단위 연산자 (bit-operators)
 * 날짜: 2026-02-24
 * 내용:
 * 1. 비트 논리 연산: &, |, ^, ~를 이용한 2진수 단위 계산
 * 2. 시프트 연산: <<, >>를 이용한 비트 이동 및 2의 거듭제곱 곱셈/나눗셈 효과
 * 3. 비트 마스킹: 특정 비트 상태 확인법 숙달
 * 4. 응용: 비트 연산을 이용한 효율적인 홀짝 판별 및 변수 값 교환(XOR Swap)
 */

#include <stdio.h>

int main() {
    unsigned char a = 10; // 2진수: 0000 1010
    unsigned char b = 12; // 2진수: 0000 1100

    printf("비트 논리 연산\n");
    printf("a & b : %d (0000 1000 -> 8)\n", a & b);   // 둘다 1일때만 1
    printf("a | b : %d (0000 1110 -> 14)\n", a | b);  // 하나라도 1이면 1 
    printf("a ^ b : %d (0000 0110 -> 6)\n", a ^ b);   // 서로 다를때만 1 
    printf("~a    : %d (1111 0101 -> 반전)\n\n", (unsigned char)~a);    // 2의 보수와 연관 -  ~n의 결과는 항상 -(n + 1)  예: ~5를 하면 -6이 나옴

    printf("비트 이동(Shift) 연산\n");
    unsigned char x = 3;  // 2진수: 0000 0011
    printf("x << 1 : %d (0000 0110 -> 6)\n", x << 1); // 2를 곱한 효과  // 왼쪽으로 한칸
    printf("x << 2 : %d (0000 1100 -> 12)\n", x << 2); // 4를 곱한 효과  // 왼쪽으로 두칸
    printf("x >> 1 : %d (0000 0001 -> 1)\n\n", x >> 1);  // 2로 나눈 효과 (소수점 버림) // 오른쪽으로 한칸

    unsigned char flags = 0b00001010; // 2번, 4번 비트가 켜진 상태
    printf("비트 마스킹 테스트\n");
    if (flags & 0b00001000) { // 4번째 비트가 1인지 확인
        printf("4번째 비트가 켜져 있습니다!\n");
    }
    // 비트 연산은 숫자를 2진수로 늘어놓고 세로로 계산!
    // <<는 2를 곱하는 것, >>는 2로 나누는 것과 같음!
    // &은 특정 비트가 살아있는 것을 확인할때 사용!

    printf("비트 연산 활용\n");
    int num = 7;
    printf("%d는 %s입니다.\n", num, (num & 1) ? "홀수" : "짝수"); // 홀짝 판별  - num % 2 == 0 을 활용하여 홀짝을 판별하지만 비트연산자가 훨씬빠

    int n1 = 10, n2 = 20;
    n1 ^= n2; n2 ^= n1; n1 ^= n2; // XOR 스와핑     -   두변수의 값을 바꾸려면 임시변수인(temp)가 필요하지만 비트연산자를 활하면 변수만으로 값을 바꾸는게 가능함!
    printf("n1: %d, n2: %d (값 교체 완료)\n", n1, n2);

    return 0;
}
