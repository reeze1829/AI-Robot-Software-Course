/*
 * 주제: 5-2. 2차원 배열 (2D Array) 심화 실습
 * 날짜: 2026-03-16
 * 내용: 
 * 1. 행렬 연산: 각 행(학생별)의 총점과 각 열(과목별)의 평균 계산
 * 2. sizeof 활용: 2차원 배열에서 행과 열의 개수를 자동으로 구하는 법
 * 3. 데이터 구조: 2차원 배열을 활용한 성적표(Table) 데이터 관리
 */

#include <stdio.h>

int main() {
    int scores[3][4] = {        // 3행 4열 배열 (3명의 학생, 4개의 과목 점수) , 메모리상에는 12개의 데이터가 일렬로 쭉 저장됨()연속성
        {85, 90, 70, 88},       // 0번 학생
        {70, 60, 55, 65},       // 1번 학생
        {95, 100, 92, 98}       // 2번 학생
    };

    // 2차원 배열 크기 계산 (공식)  전체 박스 / 한 층의 무게 = 층수(행)   ,   한 층의 무게 / 상자 하나 무게 = 호수(열)
    int rows = sizeof(scores) / sizeof(scores[0]);                  // 전체 크기 / 한 행의 크기 = 행의 개수  48 / 16 = 3(행)
    int cols = sizeof(scores[0]) / sizeof(scores[0][0]);            // 한 행의 크기 / 한 칸의 크기 = 열의 개수  16/ 4 = 4(열)
    // sizeof(scores) : 배열 전체의 용량 (12칸 * 4바이트 = 48바이트)
    // sizeof(scores[0]) : 한 행의 용량 (4칸 * 4바이트 = 16바이트)
    // sizeof(scores[0][0]) : 한 칸(요소)의 용량 (int 1개 = 4바이트)

    printf("--- 성적 분석 리포트 (%d명, %d과목) ---\n", rows, cols);
    printf("            과목1  과목2  과목3  과목4  |  총점\n");
    printf("-----------------------------------------------\n");

    for (int i = 0; i < rows; i++) {          // 학생별 총점 계산 (행 우선 순회)
        int student_sum = 0;                  // 각 학생의 줄이 시작될 때마다 합계 초기화
        printf("%d번 학생:  ", i + 1);

        for (int j = 0; j < cols; j++) {
            printf("%5d  ", scores[i][j]);    // 5칸 간격 맞춰 점수 출력
            student_sum += scores[i][j];      // 현재 학생(i)의 각 과목(j) 점수를 더함
        }
        printf(" |  %d\n", student_sum);      // 한 줄 다 돌면 총점 출력 후 줄바꿈
    }
  // 가로(학생)를 구할때는 i행을 고정하고, j열을 움직이며 세로(과)를 구할때는 j열을 고정하고 i행을 움직임!!!
    
    printf("-----------------------------------------------\n");   // 과목별 평균 계산 (세로 방향 훑기: 열 우선 순회 - 조금 더 까다로움!)
    printf("과목별 평균: ");                                       // 바깥쪽 루프가 과목(j)이고, 안쪽 루프가 학생(i)임에 주의!
    for (int j = 0; j < cols; j++) {                               // 1과목부터 4과목까지 순서대로
        int subject_sum = 0;
        for (int i = 0; i < rows; i++) {                           // 모든 학생의 해당 과목 점수를 수집
            subject_sum += scores[i][j];
        }
        printf("%.1f  ", (double)subject_sum / rows);              // (합계 / 학생수)
    }
    printf("\n");

    return 0;
}
