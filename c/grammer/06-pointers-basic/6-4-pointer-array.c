/*
 * 주제: 6-4. 포인터와 배열의 관계 (Pointer & Array)
 * 날짜: 2026-03-28
 * 핵심: 배열 이름(arr) == 0번 칸의 주소(&arr[0])
 */

#include <stdio.h>

int main() {
    int arr[3] = {10, 20, 30};     // 배열 선언 (메모리에 4바이트짜리 칸 3개가 연속으로 생김)

    // 포인터에 배열 대입
    int* ptr = arr;                 // 별도의 &를 안 붙여도 됨! 배열 이름(arr) 자체가 이미 주소니까

    printf("--- 이름의 정체 증명 ---\n");
    printf("배열 이름(arr)의 값    : %p\n", arr);
    printf("0번 칸의 주소(&arr[0]) : %p\n", &arr[0]);
    printf("=> 결과가 같음! 즉, 배열 이름은 0번 칸을 가리키는 포인터!\n\n");

    printf("--- 포인터로 배열 파헤치기 ---\n");
    printf("ptr[0] : %d\n", ptr[0]);           // 배열 인덱스 사용 
    
    printf("*(ptr + 1) : %d\n", *(ptr + 1));   // 포인터 연산 사용, ptr+1은 다음 칸 주소고, 거기 쳐들어가면(*) 20이 나옴!
    printf("*(ptr + 2) : %d\n", *(ptr + 2));

    printf("\n--- 주의할 점 (덩치 차이) ---\n");
    printf("배열(arr)의 전체 크기: %zu 바이트\n", sizeof(arr));   // arr은 배열 전체를 책임지는 이름이라 덩치가 큼 (4바이트 * 3 = 12)
    printf("포인터(ptr)의 크기   : %zu 바이트\n", sizeof(ptr));   // ptr은 그냥 주소 적힌 쪽지라 크기가 고정됨 (보통 8바이트)

    printf("\n--- [결정적 차이: 수정 가능 여부] ---\n");
    ptr++;                                                       // 포인터 변수는 다음 칸으로 이사(증감 연산) 가능!
    printf("이사 간 후 ptr이 가리키는 값: %d\n", *ptr);          // 20 출력

    // arr++;  [에러 발생!] 배열 이름은 '상수 주소'라서 이사(값 변경)가 불가능함
    // printf("arr은 이사 못 감! 0번 칸 고정 명찰이라서\n");

    return 0;
}

// [나중을 위한 3줄 요약]
// 1. arr[i] 라고 쓰면 컴퓨터는 속으로 *(arr + i) 라고 읽음 (결국 포인터 연산임)
// 2. 그래서 ptr[i] 처럼 포인터 변수를 배열처럼 써도 아무 문제 없음
// 3. 배열 이름은 이사 불가능한 고정 주소고, 포인터 변수는 주소를 바꿀 수 있는 쪽지
