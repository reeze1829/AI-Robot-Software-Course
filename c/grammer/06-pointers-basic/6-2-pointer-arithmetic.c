/*
 * 주제: 6-2. 포인터 연산 (Pointer Arithmetic)
 * 날짜: 2026-03-24
 * 핵심: 포인터의 +1은 1바이트 이동이 아니라 '자료형의 크기'만큼 점프하는 것!
 */

#include <stdio.h>

int main() {
    // [1] 각기 다른 보폭을 가진 포인터들 준비
    int     n = 10;     // 4바이트짜리 정수
    char    c = 'A';    // 1바이트짜리 문자
    double  d = 3.14;   // 8바이트짜리 실수

    int* ptr_int    = &n;
    char* ptr_char   = &c;
    double* ptr_double = &d;

    printf("=== 자료형별 주소 점프 (보폭 확인) ===\n");
    // ptr + 1을 하면 주소값이 자료형 크기(sizeof)만큼 커짐
    printf("[int]    %p + 1 = %p\n", ptr_int, ptr_int + 1); // 16진수에서 c는 12인데, 여기에 4를 더하면 16(다음 자리로 올림)이 되어 70이 됨(46c -> 470)
    printf("[char]   %p + 1 = %p\n", ptr_char, ptr_char + 1);
    printf("[double] %p + 1 = %p\n", ptr_double, ptr_double + 1);

    

    printf("\n=== 배열에서 포인터 연산 활용하기 ===\n");
    int arr[5] = {100, 200, 300, 400, 500};
    int* p_arr = arr;                                           // 배열 이름(arr)은 곧 첫 번째 칸의 주소(&arr[0])와 같음!

    printf("현재 위치(*p_arr)의 값    : %d\n", *p_arr);        // 100 출력
    printf("한 칸 옆(*(p_arr + 1))의 값: %d\n", *(p_arr + 1)); // 주소를 4바이트 밀어서 200 출력. p_arr + 1을 했을 때 다음 데이터인 200이 나온다는 건 열이 메모리에 빈틈없이 붙어 있고 포인터가 정확히 그 다음 칸 주소를 짚었다는 뜻
    printf("네 칸 옆(*(p_arr + 4))의 값: %d\n", *(p_arr + 4)); // 주소를 16바이트 밀어서 500 출력
                                                               // arr[i]는 내부적으로 *(arr + i)와 똑같이 동작함!


    printf("\n=== 포인터끼리의 뺄셈 (거리 측정) ===\n");
    int* start = &arr[0];                                       // 배열의 시작점
    int* end   = &arr[4];                                       // 배열의 끝점 (500이 든 곳)

                                                                // 주소 숫자끼리 빼면 '바이트 차이'가 아니라 '사이에 든 데이터 칸수'가 나옴!
    printf("두 주소 사이의 거리(칸수): %ld 칸\n", end - start);  // 16바이트 차이지만 결과는 4


    return 0;
}
// 결론: 포인터 연산은 단순한 숫자 합산이 아니라, 가리키는 자료형의 크기(sizeof)만큼 칸 단위로 점프하는 것임
// 보폭의 법칙: ptr + 1은 주소값이 1 늘어나는 게 아니라, int는 4, double은 8바이트씩 타입의 크기만큼 건너뜀
// 배열의 정체: arr[i]는 사실 *(arr + i)를 사람이 쓰기 편하게 줄여 놓은 별명일 뿐임
// 거리 측정: 포인터끼리 뺄셈(end - start)을 하면 바이트 수가 아니라 사이에 데이터가 몇 칸(개) 있나를 알려줌
