/*****************************************************************
 * 주제: 3-3. 생성자, 소멸자 및 초기화 리스트 (Integrated)
 * 작성일: 2026. 03. 03.
 * 설명: 
 * 1. 생성자/소멸자: 객체의 탄생과 죽음을 관리하는 생명주기(Lifecycle) 이해.
 * 2. 초기화 리스트: 대입보다 효율적인 초기화 방식 및 const/참조자 대응.
 * 3. RAII 패턴: 생성자에서 자원을 할당하고 소멸자에서 해제하여 메모리 누수 방지.
 * 4. 소멸 순서: 객체는 생성된 순서의 역순(LIFO)으로 소멸됨을 확인.
 *****************************************************************/
#include <iostream>
#include <string>

class Student {
private:
    std::string name;
    int age;
    int* scores;                                          //  동적 할당을 위한 포인터

public:
    Student() : name("미정"), age(0), scores(nullptr) {    // 기본 생성자: 매개변수가 없을 때를 대비 (생성자 오버로딩)   - (초기화 리스트 사용)
        std::cout << "  [기본 생성자 호출] 정보가 없는 학생 객체가 생성되었습니다." << std::endl;
    }                                                                   
//  초기화리스트 안좋은 예
//  Student(string n, int a) { 
//     name = n;  // 1. 빈 name이 먼저 태어남 -> 2. 그 후 n을 대입 (2단계)
//     age = a;
// }
//  좋은 예      초기화 리스트의 장점: 선언과 동시에 초기화가 이루어지므로 대입 연산보다 성능이 좋음!! 특히 const 멤버나 참조 변수는 초기화 리스트가 '유일한' 초기화 방법임.
//  Student(string n, int a) : name(n), age(a) { 
//    1. 태어나는 순간 이미 name은 n이고, age는 a임 (1단계)
// }

                                                        // 메모리 관리(RAII): 자원 획득은 초기화와 동시에!!  생성자에서 new 했으면 소멸자에서 delete 하는 것이 철칙!!
    Student(std::string n, int a) : name(n), age(a) {   // 매개변수 생성자: 객체 탄생 시 자동 호출 (초기화 리스트 사용)
        scores = new int[3]{100, 90, 80};               // 생성과 동시에 메모리를 할당받음(scores는 매개변수 생성자에서만 따로 new를 통해 할당),  
                                                        // 보통 동적 할당(new)은 생성자 몸체({ }) 안에서 하는 게 가독성 면에서 더 깔끔!!
        std::cout << "  [생성자 호출] " << name << " 학생 객체가 생성되었습니다. (메모리 할당)" << std::endl;
    }

    ~Student() {                                        // 소멸자: 객체 소멸 시 자동 호출 (메모리 해제 담당)   - 생성된 순서에 역순으로 호출됨!!
        if (scores != nullptr) {                        // ***생성자에서 빌린 메모리는 죽기 전에 반드시 반납해야 함*** (메모리 누수 방지)
            delete[] scores; 
            std::cout << "  [소멸자 호출] " << name << " 학생 객체 소멸 (메모리 해제 완료)" << std::endl;
        } else {
            std::cout << "  [소멸자 호출] " << name << " 학생 객체 소멸 (해제할 메모리 없음)" << std::endl;
        }
    }

    void info() {
        std::cout << "  [정보] 이름: " << name << ", 나이: " << age << std::endl;
    }
};

int main() {
    std::cout << " 메인 함수 시작" << std::endl;
    Student s1("철수", 20);                                  // 매개변수 생성자 사용 
    s1.info();

    {
        std::cout << "\n 지역 블록(Scope) 시작" << std::endl;
        Student s2;                                          // 기본 생성자 사용
        s2.info();
        std::cout << " 지역 블록 종료 직전" << std::endl;
    }                                                        // 블록이 끝나면서 s2는 여기서 즉시 소멸(소멸자 자동 호출) - 시스템이 수명에 맞춰 알아서 해줌!!

    std::cout << "\n 메인 함수 종료 직전" << std::endl;
    return 0; 
}                                                            // 메인 함수가 끝나면서 s1도 여기서 소멸(소멸자 자동 호출)
